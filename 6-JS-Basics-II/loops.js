/* 
Loops
- For 
- While
- Do While 
*/

// for
for (var i = 0; i <= 10; i++) {
   console.log(`for increment: ${i}`); 
}

for (var i = 10; i >= 0; i--) {
    console.log(`for decrement: ${i}`);  
 }

 // while
 var i = 1;
 while (i <= 20) {
    console.log(`while: ${i}`);
    i++;
 }

 // do-while
 do{
    console.log(`do-while: ${i}`)
    i--;
 }
 while(i >= 0);
